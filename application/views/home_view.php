            <hgroup><h2>About the electronic device</h2></hgroup>
            <p>
                An electronic calculator is typically a portable electronic device used to perform calculations, ranging from basic arithmetic to complex mathematics.The first solid state electronic calculator was created in the early 1960s.
                The pocket sized devices became available in the 1970s, especially after the first microprocessor, the Intel 4004, developed by Intel for the Japanese calculator company Busicom. 
                They later became used commonly within the petroleum industry (oil and gas).
                Modern electronic calculators vary: from cheap, give-away, credit-card-sized models to sturdy desktop models with built-in printers.
                They became popular in the mid-1970s (as integrated circuits made their size and cost small). 
                By the end of that decade, calculator prices had reduced to a point where a basic calculator was affordable to most and they became common in schools.
                Computer operating systems as far back as early Unix have included interactive calculator programs such as dc and hoc, and calculator functions are included in almost all personal digital assistant (PDA) type devices (save a few dedicated address book and dictionary devices).
